    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        // TODO add your handling code here:

        char c = evt.getKeyChar();
        String input = "";
        String trim1;
        String trim2;
        String re1 = "^[0-9]*$";
        int last;
        if (!jTextField1.getText().isEmpty()) {
            input = jTextField1.getText();
        }

        if (input.matches(re1)) { // number
            if (input.length() > 11) {
                trim1 = input.substring(0, input.length() - 1);
                input = trim1;
            }// more than 11 numbers

        } else {

            trim1 = input.substring(0, input.indexOf(c));//first to index of character;
            trim2 = input.substring(input.indexOf(c) + 1);
            input = trim1 + trim2;
        }

        jTextField1.setText(input);

        if (jTextField1.getText().isEmpty()) {
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
        } else {
            last = Integer.parseInt(jTextField1.getText());
            AttendanceLogBean login = logdao.getLatestLoginByTenant(last);
            AttendanceLogBean logout = logdao.getLatestLogoutByTenant(last);

            if (login == null && logout == null) {
                jButton2.setEnabled(false);
                jButton3.setEnabled(false);
            }
            if (login == null) {
                jButton2.setEnabled(true);
                jButton3.setEnabled(false);
            } else if (logout == null) {
                jButton2.setEnabled(false);
                jButton3.setEnabled(true);
            } else if (login.getLogID() > logout.getLogID()) {
                jButton2.setEnabled(false);
                jButton3.setEnabled(true);
            } else if (login.getLogID() < logout.getLogID()) {
                jButton2.setEnabled(true);
                jButton3.setEnabled(false);

            } else {
                jButton2.setEnabled(false);
                jButton3.setEnabled(false);
            }
        }


    }//GEN-LAST:event_jTextField1KeyReleased

